    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        if(!sl.getText().equals("") ){
            DefaultTableModel model = (DefaultTableModel) table3.getModel();
            model.setValueAt(Integer.parseInt(sl.getText()), table3.getSelectedRow(), 4);
            model.setValueAt(tt.getSelectedItem().toString(), table3.getSelectedRow(), 5);
            QLTB q = (QLTB)list.get(table3.getSelectedRow());
            q.setSoLuong(Integer.parseInt(sl.getText()));
            q.setTrangThai(tt.getSelectedItem().toString());
            list.set(table3.getSelectedRow(), q);
            ioFile.ghiFile(list, "QLTB.DAT");
        }
        else{
            JOptionPane.showMessageDialog(this, "Invalid action.");
                    
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_saveActionPerformed

