    private void add3ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add3ButtonActionPerformed
        if(!pID.getText().equals("") && !tbID.getText().equals("")&& !sl.getText().equals("")){
            int pid = Integer.parseInt(pID.getText());
        int tID = Integer.parseInt(tbID.getText());
        QLTB q = new QLTB();
        boolean bb = false, bb1 = false;
        for(GiaoTiep P: phong){
            if(((PhongHoc)P).getID() == pid){
                q.setPhongHoc(((PhongHoc)P));
                bb = true;
            }
        }
        if(!bb){
            JOptionPane.showMessageDialog(this, "Nhập sai mã phòng.");
        }
        
        for(GiaoTiep g: thietBi){
            if(((ThietBi)g).getID() == tID){
                q.setThietBi(((ThietBi)g));
                bb1 = true;
            }
        }
        if(!bb1) JOptionPane.showMessageDialog(this, "Nhập sai mã thiết bị.");
        for(GiaoTiep qq: list){
            if(((QLTB)qq).getThietBi().getID() == tID 
                    && ((QLTB)qq).getPhongHoc().getID() == q.getPhongHoc().getID()){
                JOptionPane.showMessageDialog(this, "Mã TB đã tồn tại.");
                bb1 = false;
            }
        }   
        if(bb && bb1){
            q.setSoLuong(Integer.parseInt(sl.getText()));
            q.setTrangThai(tt.getItemAt(tt.getSelectedIndex()));
            DefaultTableModel model = (DefaultTableModel) table3.getModel();
            model.addRow(new Object[]{
                q.getPhongHoc().getID(), q.getPhongHoc().getName(), q.getThietBi().getID(),
                q.getThietBi().getName(), q.getSoLuong(), q.getTrangThai()
                    
            });
            list.add(q);
            ioFile.ghiFile(list, "QLTB.DAT");
        }
        }
        else JOptionPane.showMessageDialog(this, "Hãy nhập đầy đủ thông tin.");
                
    }//GEN-LAST:event_add3ButtonActionPerformed

